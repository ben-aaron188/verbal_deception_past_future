function populate_select_from_json(ID, language) {
    var choices;
    if (language === 0) {
        choices = shuffle(choices_nl);
    } else if (language == 1) {
        choices = shuffle(choices_en);
    }
    $.each(choices, function(key, value) {
        $(ID)
            .append($("<option></option>")
                .attr("value", key)
                .text(value.option_normal));
    });
}
